/*素数和（5分）
题目内容：
我们认为2是第一个素数，3是第二个素数，5是第三个素数，依次类推。
现在，给定两个整数n和m，0<n<=m<=200，你的程序要计算第n个素数到第m个素数之间所有的素数的和，包括第n个素数和第m个素数。

输入格式:
两个整数，第一个表示n，第二个表示m。

输出格式：
一个整数，表示第n个素数到第m个素数之间所有的素数的和，包括第n个素数和第m个素数。

输入样例：
2 4

输出样例：
15
时间限制：500ms内存限制：32000kb */ 



#include <stdio.h>

int x[10001] = {};
int main()
{
    int i, j;
    int y[205] = {};
    for (i = 2; i < 10001; i++)
    {
        if (x[i] == 0)
        {
            for (j = i * 2; j < 10001; j += i)
            {

                x[j] = 1;
            }

        }
    }
    j = 1;
    for (i = 2; i < 10001 && j != 203; i++)
    {
        if (x[i] == 0)
            y[j++] = i;
    }
    int begin, end;
    scanf("%d%d", &begin, &end);
    int sum = 0;
    for (i = begin; i <= end; i++)
    {
        sum += y[i];
    }
    printf("%d\n", sum);
    
    return 0;

}
